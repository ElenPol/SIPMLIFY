
public class Buyer {
	String name;
	String id;
	String AFM;
	String phoneNumber;

	public Buyer(String name,String id,String AFM,String phoneNumber) {
	
		this.name=name;
		this.id=id;
		this.AFM=AFM;
		this.phoneNumber=phoneNumber;
}

	public String getName() {
		return name;
	}

	public String getId() {
		return id;
	}

	public String getAFM() {
		return AFM;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	
	
}


